# abs() Function
# The python abs() function is used to return the binary representation of a specified integer.
integer = -20
print(abs(integer))


# bin() Function
# The python bin() function is used to return the binary representation of a specified integer.
x = 10
y = bin(x)
print(y)


# hex() Function
# The python hex() function is used to generate hex value of an integer argument.
result = hex(1)
print(result)


# min() Function
# Python min() function is used to get the smallest element from the collection.
small = min(2225,325,2025)
print(small)
large = max(2225,325,2025)
print(large)


# oct() Function
# The python oct() function is used to get an octal value of an integer number.
# Calling function
val = oct(10)
print("Octal value of 10 :",val)


# ord() Function
# The python ord() function returns an integer representing Unicode code point for the given Unicode character.
print(ord('8'))


# round() Function
# round() function rounds off the digits of a number
print(round(10.8))


# Sum()
x = [10, 20, 30]
total = sum(x)
print("x:",x,"sum(x):",total)